package knowledgebench

import (
	"fmt"
	"sort"
	"strings"
)

// GenerateMarkdownReport builds a markdown document summarizing multiple runs.
func GenerateMarkdownReport(allMetrics []*RunMetrics) string {
	var sb strings.Builder

	sb.WriteString("# Knowledge Retrieval Parameter Experiment Report\n\n")
	sb.WriteString("Auto-generated by `knowledgebench`.\n\n")

	// summary table
	sb.WriteString("## Summary\n\n")
	sb.WriteString("| RunID | Recall@5 | Recall@10 | MRR | Latency(ms) | AI Calls | Raw(B) | Compressed(B) | Tokens |\n")
	sb.WriteString("|-------|----------|-----------|-----|-------------|----------|--------|---------------|--------|\n")

	// sort by recall@10 desc, then latency asc
	sorted := make([]*RunMetrics, len(allMetrics))
	copy(sorted, allMetrics)
	sort.Slice(sorted, func(i, j int) bool {
		if sorted[i].RecallAt10 != sorted[j].RecallAt10 {
			return sorted[i].RecallAt10 > sorted[j].RecallAt10
		}
		return sorted[i].TotalLatencyMs < sorted[j].TotalLatencyMs
	})

	for _, m := range sorted {
		sb.WriteString(fmt.Sprintf("| %s | %.3f | %.3f | %.3f | %d | %d | %d | %d | %d |\n",
			m.RunID, m.RecallAt5, m.RecallAt10, m.MRR,
			m.TotalLatencyMs, m.AICallCount,
			m.RawResultBytes, m.CompressedResultBytes, m.FinalTokenCount,
		))
	}
	sb.WriteString("\n")

	// pareto front: Recall@10 >= 95% of best, pick fastest
	if len(sorted) > 0 {
		bestRecall := sorted[0].RecallAt10
		threshold := bestRecall * 0.95

		sb.WriteString("## Pareto Front (Recall@10 >= 95% of best)\n\n")
		sb.WriteString("| RunID | Recall@10 | Latency(ms) | AI Calls |\n")
		sb.WriteString("|-------|-----------|-------------|----------|\n")

		var pareto []*RunMetrics
		for _, m := range sorted {
			if m.RecallAt10 >= threshold {
				pareto = append(pareto, m)
			}
		}
		sort.Slice(pareto, func(i, j int) bool {
			return pareto[i].TotalLatencyMs < pareto[j].TotalLatencyMs
		})
		for _, m := range pareto {
			sb.WriteString(fmt.Sprintf("| %s | %.3f | %d | %d |\n",
				m.RunID, m.RecallAt10, m.TotalLatencyMs, m.AICallCount,
			))
		}
		sb.WriteString("\n")

		if len(pareto) > 0 {
			sb.WriteString(fmt.Sprintf("**Recommended**: `%s` (Recall@10=%.3f, Latency=%dms)\n\n",
				pareto[0].RunID, pareto[0].RecallAt10, pareto[0].TotalLatencyMs,
			))
		}
	}

	// per-query breakdown for first run
	if len(sorted) > 0 && len(sorted[0].QueryResults) > 0 {
		m := sorted[0]
		sb.WriteString(fmt.Sprintf("## Per-Query Detail (best run: %s)\n\n", m.RunID))
		sb.WriteString("| QueryID | Query | Recall@5 | Recall@10 | Rank | Latency(ms) |\n")
		sb.WriteString("|---------|-------|----------|-----------|------|-------------|\n")
		for _, qr := range m.QueryResults {
			queryPreview := qr.Query
			if len(queryPreview) > 40 {
				queryPreview = queryPreview[:40] + "..."
			}
			sb.WriteString(fmt.Sprintf("| %s | %s | %.3f | %.3f | %d | %d |\n",
				qr.QueryID, queryPreview, qr.RecallAt5, qr.RecallAt10, qr.FirstHitRank, qr.LatencyMs,
			))
		}
		sb.WriteString("\n")
	}

	return sb.String()
}
